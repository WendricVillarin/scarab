import { useState } from 'react';
import './Result.css';
import RiskCard from './RiskCard.jsx';
import TextField from './TextField.jsx';

function Result({ message, onAnalyze }) {
  const [copied, setCopied] = useState(false);

  const handleCopy = () => {
    if (!message) return;
    navigator.clipboard.writeText(message);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  // Analyze message based on actual criteria from App.jsx
  const getAnalysisMetrics = () => {
    if (!message || message === 'analyzing content...') {
      return {
        plagiarism: { score: 0, detected: [], status: 'Pending' },
        originality: { score: 0, detected: [], status: 'Pending' },
        authenticity: { score: 0, detected: [], status: 'Pending' }
      };
    }

    const msg = message.toLowerCase();
    let plagiarism = { score: 90, detected: [], status: 'Legitimate' };
    let originality = { score: 90, detected: [], status: 'Legitimate' };
    let authenticity = { score: 90, detected: [], status: 'Legitimate' };

    // Generator tool detection (impacts Plagiarism Scorer)
    if (msg.includes('generated by tool')) {
      plagiarism.score = 15;
      plagiarism.detected.push('Generator Tool Used');
      plagiarism.status = 'Generic';
    }

    // Template/boilerplate detection (impacts Originality)
    if (msg.includes('unfilled template') || msg.includes('placeholder')) {
      originality.score = 25;
      originality.detected.push('Unfilled Placeholders');
      originality.status = 'Generic';
    }

    // Low word count (impacts Originality)
    if (msg.includes('insufficient word count')) {
      originality.score = Math.min(originality.score, 35);
      originality.detected.push('Low Word Count (<400)');
      originality.status = 'Generic';
    }

    // Poor structure (impacts Authenticity)
    if (msg.includes('plain design') || msg.includes('no ui structure')) {
      authenticity.score = 40;
      authenticity.detected.push('Minimal HTML Structure');
      authenticity.status = 'Generic';
    }

    // Bad TLD (impacts Authenticity)
    if (msg.includes('bad-natured tld')) {
      authenticity.score = 20;
      authenticity.detected.push('Suspicious TLD Domain');
      authenticity.status = 'Generic';
    }

    // Risky/suspicious domains (impacts all)
    if (msg.includes('known risky and suspicious')) {
      plagiarism.score = 10;
      originality.score = 15;
      authenticity.score = 12;
      plagiarism.detected.push('Known Risk Domain');
      originality.detected.push('Flagged Domain');
      authenticity.detected.push('High Risk Assessment');
      plagiarism.status = 'Generic';
      originality.status = 'Generic';
      authenticity.status = 'Generic';
    }

    // Generic policy patterns
    if (msg.includes('generic privacy policy') && !msg.includes('generated')) {
      plagiarism.score = 45;
      originality.score = 40;
      authenticity.score = 50;
      if (!originality.detected.length) originality.detected.push('Generic Template Detected');
      plagiarism.status = 'Generic';
      originality.status = 'Generic';
      authenticity.status = 'Generic';
    }

    // Uncertain results
    if (msg.includes('uncertain') || msg.includes('could not be scanned')) {
      plagiarism.score = 55;
      originality.score = 55;
      authenticity.score = 55;
      plagiarism.detected.push('Unable to Verify');
      originality.detected.push('Incomplete Analysis');
      authenticity.detected.push('Manual Review Needed');
      plagiarism.status = 'Uncertain';
      originality.status = 'Uncertain';
      authenticity.status = 'Uncertain';
    }

    // Legitimate policy detected
    if (msg.includes('appears to be a legitimate')) {
      plagiarism.score = 95;
      originality.score = 92;
      authenticity.score = 95;
      plagiarism.detected = [];
      originality.detected = [];
      authenticity.detected = [];
      plagiarism.status = 'Legitimate';
      originality.status = 'Legitimate';
      authenticity.status = 'Legitimate';
    }

    return {
      plagiarism,
      originality,
      authenticity
    };
  };

  const metrics = getAnalysisMetrics();

  const sample = [
    { 
      title: 'Plagiarism Scorer', 
      score: metrics.plagiarism.score, 
      trackers: metrics.plagiarism.detected.length ? metrics.plagiarism.detected : [], 
      status: metrics.plagiarism.status 
    },
    { 
      title: 'Content Originality', 
      score: metrics.originality.score, 
      trackers: metrics.originality.detected.length ? metrics.originality.detected : [], 
      status: metrics.originality.status 
    },
    { 
      title: 'Policy Authenticity', 
      score: metrics.authenticity.score, 
      trackers: metrics.authenticity.detected.length ? metrics.authenticity.detected : [], 
      status: metrics.authenticity.status 
    }
  ];

  return (
    <div className="result" aria-live="polite">
      <TextField onAnalyze={onAnalyze} />

      <div className="result-header">
        <h3>Analysis</h3>
        <div 
          className={`result-sub ${copied ? 'copied' : ''}`} 
          onClick={handleCopy}
          title="Click to copy"
          role="button"
          tabIndex="0"
        >
          {copied ? 'Copied to clipboard!' : (message || 'Awaiting input — enter a URL and press Analyze')}
        </div>
      </div>

      <div className="cards-grid">
        {sample.map((s, i) => (
          <RiskCard key={i} title={s.title} score={s.score} trackers={s.trackers} status={s.status} />
        ))}
      </div>
    </div>
  );
}

export default Result;